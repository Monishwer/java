package collections;

public class Bank {
	int accountNo;
	String customerName;
	double balance;
	int customerAge;
	
	public Bank(int accountNo, String customerName, double balance, int customerAge) {
		super();
		this.accountNo = accountNo;
		this.customerName = customerName;
		this.balance = balance;
		this.customerAge = customerAge;
	}

}
